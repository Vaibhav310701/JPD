import type { Metadata } from "next";
import { Poppins } from "next/font/google";
import "./globals.css";
import "slick-carousel/slick/slick.css";
import "slick-carousel/slick/slick-theme.css";
import Providers from "../components/Provider";
import Navbar from "../components/Navbar"; // Navbar remains a client component
import { ToastContainer } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

// Load Poppins font
const poppins = Poppins({
  subsets: ["latin"],
  weight: ["400", "500", "600", "700"],
  variable: "--font-poppins",
});

// Metadata
export const metadata: Metadata = {
  title: "JPD",
  description: "Generated by create next app",
};

// RootLayout as a server component
export default function RootLayout({
  children,
}: Readonly<{
  children: React.ReactNode;
}>) {
  return (
    <html lang="en">
      <body className={`${poppins.variable} antialiased`}>
        {/* Wrap with Redux Provider */}
        <Providers>
          <ToastContainer
            position="top-right" // Position of the toast
            autoClose={5000} // Auto close after 5 seconds
            hideProgressBar={false} // Show progress bar
            newestOnTop={false} // Newest toast appears at the bottom
            closeOnClick={true} // Close on click
            rtl={false} // Right to left
            pauseOnFocusLoss={true} // Pause when focus is lost
            draggable={true} // Allow dragging
            pauseOnHover={true} // Pause on hover
          />
          {/* Navbar at the top */}
          <div className="sm:h-full w-full">
            <div className="h-[54px] w-full border-b border-[#E0DEF7]">
              <Navbar />
            </div>
            {/* Page content */}
            <div className="h-[calc(100%-54px)] w-full bg-[#F7F7FD]">
              {children}
            </div>
          </div>
        </Providers>
      </body>
    </html>
  );
}
